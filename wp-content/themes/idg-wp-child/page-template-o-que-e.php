        <p>IV - contribuir para a preservação e proteção do patrimônio cultural e histórico brasileiro;</p>
        <p>V - favorecer projetos que atendam às necessidades da produção cultural e aos interesses da coletividade, aí considerados os níveis qualitativos e quantitativos de atendimentos às demandas culturais existentes, o caráter multiplicador dos projetos através de seus aspectos sócio-culturais e a priorização de projetos em áreas artísticas e culturais com menos possibilidade de desenvolvimento com recursos próprios.</p>
