        <p>Art. 4° Fica ratificado o Fundo de Promoção Cultural, criado pela Lei n° 7.505, de 2 de julho de 1986, que passará a denominar-se Fundo Nacional da Cultura (FNC), com o objetivo de captar e destinar recursos para projetos culturais compatíveis com as finalidades do Pronac e de:</p>
