        <p>Art. 18.  Com o objetivo de incentivar as atividades culturais, a União facultará às pessoas físicas ou jurídicas a opção pela aplicação de parcelas do Imposto sobre a Renda, a título de doações ou patrocínios, tanto no apoio direto a projetos culturais apresentados por pessoas físicas ou por pessoas jurídicas de natureza cultural, como através de contribuições ao FNC, nos termos do art. 5o, inciso II, desta Lei, desde que os projetos atendam aos critérios estabelecidos no art. 1o desta Lei.</p>
