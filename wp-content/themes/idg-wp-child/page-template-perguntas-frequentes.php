                     <h3><a class="collapsed" href="#this" data-toggle="collapse" data-target="#collapse-faq-6" aria-expanded="false" aria-controls="collapse-faq-6"><span>Qual é a diferença entre doação e patrocínio via incentivo fiscal da Lei de Incentivo à Cultura?</span></a></h3>
                  </div>
                  <div id="collapse-faq-6" class="collapse" aria-labelledby="faq-6" data-parent="#accordionExample">
                     <div class="card-body">
                        <p>Há duas formas de financiar um projeto aprovado pela Secretaria Especial da Cultura no mecanismo de incentivo fiscal da Lei Federal de Incentivo à Cultura: por meio de doação ou de patrocínio.</p>

                        <p>A doação é um repasse sem retorno de imagem para o incentivador. É um apoio que resulta apenas da decisão de aplicar parcela do imposto de renda devido em um projeto cultural para o qual a pessoa ou empresa queira contribuir.</p>

                        <p>O patrocínio é um repasse com retorno de imagem. Além de viabilizar a realização de um projeto, o patrocinador se beneficia de estratégias de comunicação, assinando o patrocínio com sua marca e inserindo sua imagem associada ao projeto selecionado, conforme Artigo 23, da Lei 8.313/91.</p>

                        <p>A doação ou patrocínio não podem ser feitas a projeto de pessoa ou instituição vinculada ao apoiador. O parágrafo 1º do Artigo 27 da Lei 8.313/91 apresenta essa lista.</p>
                     </div>
