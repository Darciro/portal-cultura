            <div id="accordionExample" class="accordion">
               <div class="card">
                  <div id="faq-5" class="card-header">
                     <h3><a class="collapsed" href="#this" data-toggle="collapse" data-target="#collapse-faq-5" aria-expanded="false" aria-controls="collapse-faq-5"><span>Quanto uma empresa ou cidadão pode destinar de seu imposto de renda a um projeto da Lei Federal de Incentivo à Cultura?</span></a></h3>
                  </div>
                  <div id="collapse-faq-5" class="collapse" aria-labelledby="faq-5" data-parent="#accordionExample">
                     <div class="card-body">
                        <p>A legislação tributária estabelece um limite percentual do imposto de renda que a pessoa deve pagar em determinado exercício para que as pessoas físicas ou jurídicas apoiem projetos culturais:</p>

                        <ul>
                          <li>Pessoas jurídicas: até 4% do imposto devido</li>
                          <li>Pessoas físicas: até 6% do imposto devido</li>
                        </ul>

                        <p>Respeitando o percentual máximo, os incentivadores poderão ter parte do valor desembolsado ou o valor integral deduzido do imposto devido. Apenas as pessoas físicas que realizam a declaração de imposto de renda e a as empresas tributadas em lucro real podem apoiar os projetos via Lei Federal de Incentivo à Cultura.</p>

                        <p><strong>Exemplo 1:</strong></p>
                        <p>Pessoa jurídica: uma empresa que tenha que pagar R$ 100 mil de imposto de renda, poderá apoiar projetos culturais via Lei Federal de Incentivo à Cultura num valor de até R$ 4 mil.</p>

                        <p>Pessoa física: se um cidadão tiver que pagar R$ 5 mil de imposto de renda, ele poderá apoiar projetos culturais via Lei Federal de Incentivo à Cultura num valor de até R$ 300.</p>

                        <p>Dentro desses limites, o valor a ser restituído depende da faixa de renúncia na qual se enquadra o projeto cultural a ser apoiado. Os projetos podem ser enquadrados no Artigo 18 da Lei, que permite restituição de 100% do valor financiado dentro do limite acima estipulado. </p>

                        <p>Para os apoios a projetos enquadrados no Artigo 26 da Lei, o percentual varia conforme a natureza do investimento (doação ou patrocínio) e o tipo de pessoa (física ou jurídica).</p>

                        <p>Faixas de Renúncia<br/> Artigo 18</p>

                        <ul>
                          <li>Os projetos enquadrados no Artigo 18 da Lei 8.313/91 permitem abatimento de 100% do valor efetivamente despendido pelo patrocinador ou apoiador para projetos de:</li>
                          <li>artes cênicas;</li>
                          <li>livros de valor artístico, literário ou humanístico;</li>
                          <li>música erudita ou instrumental;</li>
                          <li>exposições de artes visuais;</li>
                          <li>doações de acervos para bibliotecas públicas, museus, arquivos públicos e cinematecas, bem como treinamento de pessoal e aquisição de equipamentos para a manutenção desses acervos;</li>
                          <li>produção de obras cinematográficas e videofonográficas de curta e média metragem e preservação e difusão do acervo audiovisual;
                          preservação do patrimônio cultural material e imaterial;</li>
                          <li>construção e manutenção de salas de cinema e teatro, que poderão funcionar também como centros culturais comunitários, em municípios com menos de cem mil habitantes.</li>
                        </ul>

                        <p>Se uma pessoa física ou jurídica quiser financiar um desses projetos aprovados via Lei Federal de Incentivo à Cultura tanto como doação quanto como patrocínio, poderá renunciar de 100% do valor apoiado, dentro do limite de 4% já estipulado pela legislação tributária.</p>

                        <p><strong>Exemplo 2:</strong></p>
                        <p>No caso, se a pessoa quiser doar R$ 100 para um projeto, o valor da renúncia do pagamento do imposto de renda será integral. Ao invés de pagar esses R$ 100 à Receita Federal, é facultado o seu investimento em um projeto cultural aprovado pela Secretaria Especial da Cultura do Ministério da Cidadania.</p>

                        <p>No caso de uma empresa tributada pelo lucro real, a renúncia para incentivo a um projeto da Lei Federal de Incentivo à Cultura também é de 100%. De acordo com o exemplo estabelecido no exemplo 1, a doação ou patrocínio deverá atingir o valor máximo de R$ 4 mil para não ultrapassar o limite hipotético estipulado conforme a lei tributária.</p>

                        <p>Artigo 26</p>
                        <p>Todos os demais projetos que não estão previstos no Artigo 18 se enquadram no Artigo 26, que prevê renúncia fiscal parcial, conforme o tipo de pessoa (física ou jurídica) e a natureza do investimento:</p>
                        <p>Para a pessoa física</p>
                        <p>Se uma pessoa física quiser financiar um projeto da Lei Federal de Incentivo à Cultura como doação, poderá renunciar de 80% do valor doado, dentro do limite de 4% já estipulado pela legislação tributária. Já no apoio como patrocínio, o percentual de renúncia é de 60%.</p>

                        <p><strong>Exemplo 3:</strong>
                        <p>Ao doar R$ 100 para um projeto cultural, a pessoa física terá 80% de renúncia no imposto de renda, ou seja, R$ 80. O valor não ultrapassa o limite hipotético de R$ 300 apresentado no exemplo 1.</p>
                        <p>Para os mesmos R$ 100 apoiados como patrocínio, a renúncia no imposto de renda será de R$ 60.</p>
                        <p>Para a pessoa jurídica</p>
                        <p>No caso de uma empresa tributada em lucro real, a renúncia para doação a um projeto da Lei Federal de Incentivo à Cultura é de 40%. Já, se a empresa quiser ter sua imagem associada ao projeto cultural, o apoio se dá via patrocínio e o percentual de renúncia é de 30%.</p>

                        <p><strong>Exemplo 4:</strong></p>
                        <p>Se a doação for no valor de R$ 10 mil, a renúncia será de R$ 4 mil. O valor está de acordo com o limite hipotético estabelecido no exemplo 1.
                        Já se a empresa quiser ter sua imagem associada ao projeto selecionado, o apoio se dá via patrocínio e o percentual de renúncia é de 30%, ou seja, de até R$ 3 mil.</p>
                         
                        <p>Despesa operacional</p>
                        <p>Ao apoiar projetos enquadrados no Artigo 26, a pessoa jurídica tributada com base no lucro real ainda poderá abater as doações e patrocínios como despesa operacional. Nesse caso, haverá diminuição do lucro resultante e, consequentemente, do imposto de renda a pagar.</p>

                        <p>Segundo a Receita Federal, são “operacionais as despesas não computadas nos custos, necessárias à atividade da empresa e à manutenção da respectiva fonte produtora, entendendo-se como necessárias as pagas ou incorridas para a realização das transações ou operações exigidas pela atividade da empresa.”</p>

                        <p>Assim, o percentual de dedução do imposto de renda para apoiar projetos do Artigo 26 da Lei Federal de Incentivo à Cultura atinge, na prática, os seguintes percentuais para a pessoa jurídica:</p>

                        <ul>
                          <li>Para doação, os percentuais a serem deduzidos variam entre 65% e 70%.</li>
                          <li>Para apoio a projetos como patrocínio o percentual é entre 55% e 60%.</li>
                        </ul>

