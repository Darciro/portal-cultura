                     <h3><a class="collapsed" href="#this" data-toggle="collapse" data-target="#collapse-faq-27" aria-expanded="false" aria-controls="collapse-faq-27"><span>Sobre o anúncio institucional: esta ação está respaldada pelo item I – Art. 44 da Normativa? Em caso afirmativo, as peças produzidas devem ser submetidas à aprovação do MinC? As logomarcas do MinC devem constar nos anúncios institucionais? A menção ao espetáculo /projeto ou frase de apoio à cultura, deve ser inserida na arte obrigatoriamente?</span></a></h3>
                  </div>
                  <div id="collapse-faq-27" class="collapse" aria-labelledby="faq-27" data-parent="#accordionExample">
                     <div class="card-body">
                        <p>Sim, o anúncio institucional está contemplado entre as ações adicionais que podem ser realizadas pelo patrocinador, pelos proponentes ou pelos captadores, desde que os proponentes estejam de acordo com a ação. Conforme o inciso II do art. 47 do Decreto nº 5.761/2006, as logomarcas da Secretaria Especial da Cultura devem constar nos anúncios institucionais, de acordo com as normas de uso do manual de identidade visual da Secretaria Especial da Cultura do Ministério da Cidadania, disponível no link http://www.cultura.gov.br/snc/logomarca-e-manual-de-identidade. Como acontece com as peças de promoção de projetos aprovados pela Lei Federal de Incentivo à Cultura, é necessário que a Secretaria Especial da Cultura do Ministério da Cidadania aprove o uso de sua logomarca nas peças.</p>
