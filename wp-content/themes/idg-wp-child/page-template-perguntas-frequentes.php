                     <h3><a class="collapsed" href="#this" data-toggle="collapse" data-target="#collapse-faq-25" aria-expanded="false" aria-controls="collapse-faq-25"><span>É possível que os patrocinadores, com recursos próprios, promovam a exposição de seus produtos no local de realização do evento, bem como a comercialização dos mesmos, dentro da especificação "promoção do patrocinador e de suas marcas e produtos?</span></a></h3>
                  </div>
                  <div id="collapse-faq-25" class="collapse" aria-labelledby="faq-25" data-parent="#accordionExample">
                     <div class="card-body">
                        <p>Sim é possível. De acordo com o inciso I do art. 44 da Instrução Normativa nº 05/2017, não configuram vantagem indevida ações adicionais realizadas pelo patrocinador, pelos proponentes ou pelos captadores cujo objetivo seja a prospecção comercial, a criação de programas de relacionamento ou de ampliação da divulgação, bem como a promoção do patrocinador, de suas marcas e produtos, desde que os proponentes estejam de acordo com a ações e que estas sejam realizadas com recursos que não tenham sido arrecadados por meio de incentivo fiscal. É importante ressaltar que, para esses produtos e ações, também valem as regras de uso da logomarca da Secretaria Especial da Cultura do Ministério da Cidadania, conforme especificado no inciso II do art. 47 do Decreto nº 5.761/2006, e seguindo as normas do manual de identidade visual da Secretaria Especial da Cultura do Ministério da Cidadania, disponível no link http://www.cultura.gov.br/snc/logomarca-e-manual-de-identidade.</p>
