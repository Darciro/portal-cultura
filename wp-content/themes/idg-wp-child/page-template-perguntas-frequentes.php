                     <h3><a class="collapsed" href="#this" data-toggle="collapse" data-target="#collapse-faq-24" aria-expanded="false" aria-controls="collapse-faq-24"><span>É possível que o patrocinador faça a prospecção da sua marca em um projeto realizado com incentivo fiscal?</span></a></h3>
                  </div>
                  <div id="collapse-faq-24" class="collapse" aria-labelledby="faq-24" data-parent="#accordionExample">
                     <div class="card-body">
                        <p>O patrocinador pode ativar sua marca em um projeto, desde que as ações sejam realizadas com recursos próprios, não decorrentes do incentivo fiscal, nas seguintes situações:</p>
                        <p>Veiculação da imagem institucional ou nome do incentivador em peças de divulgação além das aprovadas pelo Secretaria Especial da Cultura do Ministério da Cidadania;</p>
                        <p>Fornecimento de produtos ou serviços do patrocinador ao projeto cultural, desde que comprovada a maior economicidade ou exclusividade;</p>
                        <p>Programas de relacionamento e ações similares de prospecção de potenciais incentivadores individuais e quaisquer ações de ativação de marcas, desde que realizadas com recursos próprios do patrocinador;</p>
                        <p>Concessão de acesso a ensaios, apresentações, visitas ou quaisquer atividades associadas ao projeto cultural;</p>
                        <p>Comercialização do produto cultural em condições mais favoráveis a público determinado em função do incentivador;</p>
                        <p>Delimitação de espaços a público determinado em função do incentivador.</p>
                     </div>
